
'''(Casting in python is therefore done using constructor functions:)'''

# >>>>> INT() <<<<< # :  
x = int(1)  
y = int(2.8)
z = int("3")

print(x) # Output: 1
print(y) # Output: 2
print(z) # Output: 3



# >>>>> FLOAT() <<<<< # : 
x = float(1)    
y = float(2.8)  
z = float("3")  
w = float("4.2")

print(x) # Output: 1.0
print(y) # Output: 2.8
print(z) # Output: 3.0
print(w) # Output: 4.2



# >>>>> STR() <<<<< # : 
x = str("s1")
y = str(2)  
z = str(3.0)  

print(x) # Output: 's1'
print(y) # Output: '2'
print(z) # Output: '3.0'