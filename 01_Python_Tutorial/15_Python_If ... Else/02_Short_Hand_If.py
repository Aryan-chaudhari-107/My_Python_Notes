
# >>>>> SHORT HAND IF <<<<< # :
a = 10
b = 8
if a > b: print("a is greater than b")


# >>>>> SHORT HAND IF... ELSE <<<<< # :
''' One line if else statement: '''
a = 2
b = 330
print("A") if a > b else print("B")


''' You can also have multiple else statements on the same line: '''
a = 330
b = 330
print("A") if a > b else print("=") if a == b else print("B")