
# >>>>> MANY VALUE TO MULTIPLE VARIABLES <<<<< # :
x, y, z = "Orange", "Banana", "Cherry"
print(x)
print(y)
print(z)



# >>>>> ONE VALUE TO MULTIPLE VARIABLES <<<<< # :
x = y = z = "Orange"
print(x)
print(y)
print(z)



# >>>>> UNPACK A COLLECTION <<<<< # :
fruits = ["apple", "banana", "cherry"]
x, y, z = fruits
print(x)
print(y)
print(z)